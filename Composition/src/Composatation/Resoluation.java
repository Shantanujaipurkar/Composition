package Composatation;

public class Resoluation {

	
	private int width;
	private int height;
	
	
	public Resoluation(int width, int height) {
		super();
		this.width = width;
		this.height = height;
	}


	public int getWidth() {
		return width;
	}


	public int getHeight() {
		return height;
	}

}
